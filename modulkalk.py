# tworzymy definicje kolejnych operacji matematycznych
# dodajemy parametry funkcji, będące zmiennymi przez, które to zmienne będziemy przekazywali dane do obliczeń pobrane z klawiatury
# przy użyciu słowa return, będziemy zwracali dane będące wynikiem działania danej funkcji
def dodawanie(pierwszaLiczba, drugaLiczba):
    rodzajDzilania = pierwszaLiczba + drugaLiczba
    return rodzajDzilania
 
def odejmowanie(pierwszaLiczba, drugaLiczba):
    rodzajDzilania = pierwszaLiczba - drugaLiczba
    return rodzajDzilania
 
def mnozenie(pierwszaLiczba, drugaLiczba):
    rodzajDzilania = pierwszaLiczba * drugaLiczba
    return rodzajDzilania
 
def dzielenie(pierwszaLiczba, drugaLiczba):
    rodzajDzilania = pierwszaLiczba / drugaLiczba
    return rodzajDzilania
